def get_comorbidities(disease, min_strength=1):
    """Get all diseases linked to this one, with relation and strength."""
    results = []
    for a, b, rel, strength in COMORBIDITY:
        if strength < min_strength:
            continue
        if a == disease:
            results.append((b, rel, strength))
        elif b == disease:
            results.append((a, rel, strength))
    return results
 
 
def expand_diseases(user_diseases, min_strength=2):
    """
    Given a list of user diseases, return expanded set including comorbidities.
    Returns dict: {disease: "reason"} — reason explains WHY it was added.
    Only adds diseases the user didn't already list.
    """
    expanded = {}
    for d in user_diseases:
        expanded[d] = "ব্যবহারকারী জানিয়েছেন"  # "user reported"
    for d in user_diseases:
        for linked, rel, strength in get_comorbidities(d, min_strength):
            if linked not in expanded:
                if rel == "increases_risk":
                    reason = f"{d} থেকে ঝুঁকি বাড়ে"
                else:
                    reason = f"{d} এর সাথে সম্পর্কিত"
                expanded[linked] = reason
    return expanded
 
 
