def _parse(csv_str):
    """Split CSV, strip parenthetical qualifiers, apply aliases."""
    if not csv_str:
        return []
    out = []
    for raw in csv_str.split(","):
        s = raw.strip()
        if not s:
            continue
        # remove anything in parens: "ডায়াবেটিস (অতিরিক্ত)" → "ডায়াবেটিস"
        s = re.sub(r"\s*\(.*?\)\s*", "", s).strip()
        s = _ALIAS.get(s, s)
        if s:
            out.append(s)
    return out
 
 
