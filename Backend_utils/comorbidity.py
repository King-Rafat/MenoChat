def get_warnings_fuzzy(food_name):
    """
    Strict fuzzy match. Returns (matched_db_name, warnings_list) or (None, None).
 
    Designed to avoid false positives in medical advice. Prior versions
    matched "গরম সেঁক" (heating pad, not a food) to "গরম গরম জিলাপি"
    just because the word "গরম" appeared in both — giving dangerous,
    wrong warnings. This version refuses that kind of match.
 
    Level 1 — Exact match on the whole string.
    Level 2 — Multi-word queries only: EVERY query token must appear as a
              whole word in the DB name, AND the DB name must have at most
              one extra token beyond the query. This blocks short queries
              like "গরম" from latching onto long compound DB entries.
 
    Single-word queries → ONLY exact match. If the word isn't a standalone
    DB entry, we return nothing. Safer to stay silent than to guess.
    """
    food_name = food_name.strip()
    if not food_name:
        return None, None
 
    # Level 1 — exact match
    for name, cat, bad, good in FOODS:
        if name == food_name:
            return name, _parse(bad)
 
    query_tokens = food_name.split()
 
    # Single-word queries — exact only. No Level 2. This is the key safety rule.
    if len(query_tokens) < 2:
        return None, None
 
    # Level 2 — every query token must appear as a whole word AND
    # DB name can have at most 1 extra token (prevents "দুধ চা" matching
    # a 5-token compound entry that happens to contain দুধ and চা)
    candidates = []
    qset = set(query_tokens)
    for name, cat, bad, good in FOODS:
        db_tokens = name.split()
        if all(qt in db_tokens for qt in query_tokens):
            extra = len(db_tokens) - len(query_tokens)
            if extra <= 1:
                candidates.append((name, _parse(bad)))
    if candidates:
        # Prefer the DB name closest in length to the query (most specific)
        candidates.sort(key=lambda x: abs(len(x[0]) - len(food_name)))
        return candidates[0]
 
    return None, None
 
 
