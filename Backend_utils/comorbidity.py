def get_warnings_by_category(name):
    """
    Generic umbrella lookup. If `name` matches a CATEGORY (বাদাম, শাক,
    মাছ, ডাল, ফল ...) or a category alias (শাকসবজি → শাক+সবজি),
    aggregate warnings across every food in those categories.
 
    Returns a deduplicated list of diseases, or None if `name` isn't a
    known category. Safe because categories are curated.
    """
    name = name.strip()
    if not name:
        return None
 
    # Resolve alias to one or more canonical categories
    target_cats = _CATEGORY_ALIAS.get(name, [name])
 
    warnings = []
    seen = set()
    found_category = False
    for _, cat, bad, _ in FOODS:
        if cat in target_cats:
            found_category = True
            for d in _parse(bad):
                if d not in seen:
                    seen.add(d)
                    warnings.append(d)
    return warnings if found_category else None
 
 
