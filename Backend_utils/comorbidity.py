# Pre-sort foods by name length (longest first) for substring matching
_FOODS_BY_LEN = sorted(FOODS, key=lambda f: -len(f[0]))
 
 
def extract_foods_from_text(text):
    """
    Scan Bangla LLM output for known food names.
    Returns list of (food_name, category, worsens_list, helps_list).
    Longest-first matching avoids "ভাত" stealing from "সাদা ভাত".
    """
    found = []
    seen = set()
    for name, cat, bad, good in _FOODS_BY_LEN:
        if len(name) < 3:       # skip tiny ambiguous strings
            continue
        if name in text and name not in seen:
            found.append((name, cat, _parse(bad), _parse(good)))
            seen.add(name)
    return found
 
 
def annotate_llm_response(llm_text, user_diseases=None):
    """
    THE KEY FUNCTION for on-the-go use.
 
    Takes the raw Bangla text from the LLM, finds every food it mentioned,
    and builds a warning block the app can append or show as a tooltip.
 
    If user_diseases is given → also flags comorbid risks.
 
    Returns: (warnings_text, foods_found_count)
    """
    foods = extract_foods_from_text(llm_text)
    if not foods:
        return ("", 0)
 
    # If user has known diseases, expand with comorbidities
    all_diseases = set()
    if user_diseases:
        expanded = expand_diseases(user_diseases, min_strength=2)
        all_diseases = set(expanded.keys())
 
    lines = []
    for name, cat, worsens, helps in foods:
        parts = []
        if worsens:
            # Highlight diseases the user actually has or is at risk for
            if all_diseases:
                personal = [d for d in worsens if d in all_diseases]
                other = [d for d in worsens if d not in all_diseases]
                if personal:
                    parts.append(f"🚨 আপনার জন্য সতর্কতা: {', '.join(personal)}")
                if other:
                    parts.append(f"⚠️ এছাড়াও প্রভাবিত: {', '.join(other[:5])}")
            else:
                # No known diseases — show all warnings generically
                parts.append(f"⚠️ যাদের {', '.join(worsens[:5])} আছে তারা সতর্ক থাকুন")
        if helps:
            parts.append(f"✅ উপকারী: {', '.join(helps[:5])}")
 
        if parts:
            lines.append(f"  {name} ({cat}): " + " | ".join(parts))
 
    if not lines:
        return ("", len(foods))
 
    header = "🍽️ উল্লেখিত খাবারের তথ্য:\n"
    return (header + "\n".join(lines), len(foods))
 
 
